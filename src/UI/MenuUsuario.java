    private void BotonCrearTiqueteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCrearTiqueteActionPerformed

        try {
            Tiquete newTiquete = new Tiquete();

            newTiquete.setNombreEmpleado(TextFieldNombre.getText());
            newTiquete.setEmail(TextFieldEmail.getText());
            newTiquete.setDescripcionProblema(TextAreaDescripcion.getText());
            newTiquete.setIdEmpleado(Integer.parseInt(TextFieldNumEmpleado.getText()));
            newTiquete.setTelefono(Integer.parseInt(TextFieldTelefono.getText()));

            String aviso = "Se ha guardado satisfactoriamente";

            laCola.encola(new NodoColaTiquete(newTiquete));

            this.dispose();
            MenuUsuario menuUsuario = new MenuUsuario(laCola, tiquete);
            menuUsuario.setVisible(true);
            JOptionPane.showMessageDialog(null, aviso);
            
            System.out.println(laCola);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se ha podido crear el tiquete \nPor favor intente de nuevo, revisando cada uno de los datos");
        }
    }//GEN-LAST:event_BotonCrearTiqueteActionPerformed

