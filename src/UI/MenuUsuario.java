    private void BotonMostrarTiquetesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonMostrarTiquetesActionPerformed
        try {
            int IDaBuscar = Integer.parseInt(TextFieldIDEmpleHistorial.getText());
            if (laCola.existe(IDaBuscar)) {
                TiquetesActivos.setText(laCola.buscar(IDaBuscar).toString());
            } else {
                JOptionPane.showMessageDialog(null, "Numero no registrado");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Digite un ID por favor");
        }
    }//GEN-LAST:event_BotonMostrarTiquetesActionPerformed

